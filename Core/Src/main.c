void Apply_Step(uint8_t step) {

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, 0);

    if(step_table[step][0] == 1)
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, motor_speed);

    if(step_table[step][1] == 1)
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, motor_speed);

    if(step_table[step][2] == 1)
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, motor_speed);
}

void Read_Hall_Sensors() {
    uint8_t hall_u = HAL_GPIO_ReadPin(HALL_U_PORT, HALL_U_Pin);
    uint8_t hall_w = HAL_GPIO_ReadPin(HALL_W_PORT, HALL_W_Pin);

    uint8_t hall_value = (hall_u << 2) | (hall_v << 1) | hall_w;

    switch(hall_value) {
        case 0b101: current_step = 0; break; // 101 ->  0
        case 0b100: current_step = 1; break; // 100 ->  1
        case 0b110: current_step = 2; break; // 110 ->  2
        case 0b010: current_step = 3; break; // 010 ->  3
        case 0b011: current_step = 4; break; // 011 ->  4
        case 0b001: current_step = 5; break; // 001 ->  5
    }

    Apply_Step(current_step);
}
